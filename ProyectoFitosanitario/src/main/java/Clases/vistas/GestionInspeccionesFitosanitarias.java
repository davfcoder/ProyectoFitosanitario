    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        InspeccionFitosanitariaDAO dao = new InspeccionFitosanitariaDAO();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int[] selected = jTable1.getSelectedRows();
        if (selected.length < 1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar uno o m谩s Inspecciones para eliminar.", "AVISO", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "驴Est谩 seguro de eliminar las inspecciones seleccionadas?",
                "Confirmar eliminaci贸n",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
        );

        if (confirmacion != JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, "Eliminaci贸n cancelada por el usuario.");
            return;
        }

        int eliminados = 0;
        java.util.Arrays.sort(selected);

        //  Recorremos de atr谩s hacia adelante para evitar conflictos de 铆ndice
        for (int idx = selected.length - 1; idx >= 0; idx--) {
            int row = selected[idx];
            try {
                Object valorCelda = jTable1.getValueAt(row, 0);

                if (valorCelda == null) {
                    JOptionPane.showMessageDialog(
                            this,
                            "La fila " + (row + 1) + " no tiene un ID v谩lido (columna vac铆a).",
                            "Advertencia",
                            JOptionPane.WARNING_MESSAGE
                    );
                    continue;
                }

                String idInspeccionFitosanitaria = valorCelda.toString();

                boolean ok = dao.eliminar(idInspeccionFitosanitaria);
                if (ok) {
                    model.removeRow(row);
                    eliminados++;
                } else {
                    JOptionPane.showMessageDialog(
                            this,
                            "No se pudo eliminar la inspecci贸n con ID: " + idInspeccionFitosanitaria,
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(
                        this,
                        "Error inesperado al eliminar la fila " + (row + 1) + ": " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                );
            }
        }

        if (eliminados > 0) {
            JOptionPane.showMessageDialog(this, eliminados + " Inspeccione(s) eliminadas correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se elimin贸 ninguna inspecci贸n.");
        }

    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona una inspecci贸n de la tabla para editarlo.");
            return;
        }

        // Supongamos que la columna 0 tiene el n煤mero de identificaci贸n
        String idInspeccionFitosanitaria = jTable1.getValueAt(fila, 0).toString();
        String lugarInspec = jTable1.getValueAt(fila, 6).toString();
        String loteLugar = jTable1.getValueAt(fila, 7).toString();

        // Buscar la Inspecci贸n en la BD
        try {
                Clases.dao.InspeccionFitosanitariaDAO buscarId = new Clases.dao.InspeccionFitosanitariaDAO();
            Clases.modelo.InspeccionFitosanitaria inspeccionfitosanitaria = buscarId.buscarPorId(idInspeccionFitosanitaria);

            if (inspeccionfitosanitaria != null) {
                // Crear el panel EditInspeccionFitosanitaria y pasarle la inspeccion
                inspeccionfitosanitaria.setNombreLugarProduccion(lugarInspec);
                inspeccionfitosanitaria.setNumeroLote(loteLugar);
                EditInspeccionFitosanitaria panelEditar = new EditInspeccionFitosanitaria();
                panelEditar.setInspeccionFitosanitaria(inspeccionfitosanitaria);

                // Mostrar el panel en el Dashboard
                Dashboard.ShowJPanel(panelEditar);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr贸 la inspecci贸n seleccionado.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al obtener la inspecci贸n: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

