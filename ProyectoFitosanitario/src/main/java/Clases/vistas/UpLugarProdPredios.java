    private void btnAsociarPrediosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsociarPrediosActionPerformed
        PredioDAO daoPredioLP = new PredioDAO();
        DefaultTableModel model = (DefaultTableModel) tblPrediosLP.getModel();

        int[] selectedRows = tblPrediosLP.getSelectedRows();

        if (selectedRows.length < 1) {
            JOptionPane.showMessageDialog(this,
                    "Debes seleccionar uno o m√°s predios para asociarlos al lugar.",
                    "Sin predio seleccionado",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            int asociados = 0;
            // Ordenamos los √≠ndices de mayor a menor para eliminar correctamente del modelo
            java.util.Arrays.sort(selectedRows);

            // Iteramos de atr√°s hacia adelante para que la eliminaci√≥n de filas no afecte los √≠ndices.
            for (int idx = selectedRows.length - 1; idx >= 0; idx--) {
                int row = selectedRows[idx];

                try {
                    // Columna 0 contiene el ID_PREDIO
                    String idPredio = tblPrediosLP.getValueAt(row, 0).toString();

                    // Columna 1 contiene el NUM_PREDIAL (solo para mensaje de error)
                    String numPredial = tblPrediosLP.getValueAt(row, 1).toString();

                    // ‚≠ê LLAMAR AL NUEVO M√âTODO PARA ASOCIAR EL PREDIO
                    boolean ok = daoPredioLP.asociarLugarProduccion(idPredio, this.idLugarProduccion);

                    if (ok) {
                        model.removeRow(row); // Quitar el predio de la tabla de "no asociados"
                        asociados++;
                    } else {
                        // El error se muestra dentro del m√©todo DAO, pero puedes poner un mensaje extra aqu√≠
                        JOptionPane.showMessageDialog(
                                this,
                                "No se pudo asociar el predio " + numPredial + " al lugar de producci√≥n.",
                                "Error de Asociaci√≥n",
                                JOptionPane.ERROR_MESSAGE
                        );
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(
                            this,
                            "Error inesperado al asociar predio en la fila " + row + ": " + e.getMessage(),
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );
                }
            }

            if (asociados > 0) {
                JOptionPane.showMessageDialog(
                        this,
                        asociados + " predio(s) asociado(s) correctamente"
                );

                Object[] opciones = {"Asociar m√°s predios", "Men√∫ Principal"};
                // 2. Muestra el cuadro de di√°logo con las opciones personalizadas
                int seleccion = JOptionPane.showOptionDialog(
                        this, // Componente padre
                        "¬øDeseas volver al men√∫ principal o asociar otro predio al lugar de producci√≥n?", // Mensaje
                        "Guardado Exitoso", // T√≠tulo de la ventana
                        JOptionPane.YES_NO_OPTION, // Tipo de opciones (usamos YES_NO_OPTION como base)
                        JOptionPane.QUESTION_MESSAGE, // Tipo de icono (o INFORMATION_MESSAGE, como prefieras)
                        null, // Icono personalizado (null para usar el predeterminado)
                        opciones, // Array de los textos de los botones
                        opciones[0] // Opci√≥n predeterminada (Asociar Otra Plaga)
                );
                // 3. Maneja la respuesta del usuario
                // showOptionDialog devuelve el √≠ndice del bot√≥n presionado (0 para la primera opci√≥n, 1 para la segunda)
                if (seleccion == 1) {
                    Dashboard.ShowJPanel(new GestionLugaresProduccion());
                }

            } else {
                // Si hay un error, el mensaje de error se mostrar√° en el bucle
                JOptionPane.showMessageDialog(
                        this,
                        "No se asoci√≥ ning√∫n predio."
                );
            }
        }


    }//GEN-LAST:event_btnAsociarPrediosActionPerformed

