    private void cargarPlagasNoAsociadas() {
        PlagaDAO dao = new PlagaDAO();

        listPlagas.removeAll();

        dao.listarNoAsociadasAEspecie(idEspecie).forEach((p) -> {
            listPlagas.add(
                p.getIdPlaga() + " - " +
                p.getNombreComun()+ " (" + p.getNomEspecie()+ ")"
            );
        });
    }


