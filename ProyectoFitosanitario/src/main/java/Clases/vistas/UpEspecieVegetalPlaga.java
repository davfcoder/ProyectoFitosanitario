    private void btnGuardarEspecieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEspecieActionPerformed
        String[] seleccion = listPlagas.getSelectedItems();
        
        if (seleccion.length == 0) {

            JOptionPane.showMessageDialog(this,
                "Debe seleccionar al menos una plaga antes de guardar.",
                "Sin plagas seleccionadas",
                JOptionPane.WARNING_MESSAGE);
            return; // no continúa si falta información
        }
        try {
            EspeciePlagaDAO plagaDao = new EspeciePlagaDAO();

            for (String item : seleccion) {
                // item viene con formato "12 - NombreCientífico (NombreComún)"

                String idPlaga = item.split(" - ")[0].trim();

                plagaDao.insertar(idEspecie, idPlaga);
            }

            JOptionPane.showMessageDialog(this, 
                "Plagas asociadas correctamente a la especie vegetal");

            // Opcional: limpiar selección
            listPlagas.deselect(listPlagas.getSelectedIndex());

            // Opcional: recargar lista para desaparecer las plagas ya asociadas
            cargarPlagasNoAsociadas();
            Dashboard.ShowJPanel(new GestionEspeciesVegetales());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error al guardar: " + e.getMessage());
            e.printStackTrace();
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarEspecieActionPerformed

