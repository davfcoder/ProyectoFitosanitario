    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        LoteDAO dao = new LoteDAO();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int[] selected = jTable1.getSelectedRows();
        if (selected.length < 1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar uno o m치s Lotes para eliminar.", "AVISO", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "쮼st치 seguro de eliminar los lotes seleccionadas?",
                "Confirmar eliminaci칩n",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
        );

        if (confirmacion != JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, "Eliminaci칩n cancelada por el usuario.");
            return;
        }

        int eliminados = 0;
        java.util.Arrays.sort(selected);

        // 游댳 Recorremos de atr치s hacia adelante para evitar conflictos de 칤ndice
        for (int idx = selected.length - 1; idx >= 0; idx--) {
            int row = selected[idx];
            try {
                Object valorCelda = jTable1.getValueAt(row, 0);

                if (valorCelda == null) {
                    JOptionPane.showMessageDialog(
                            this,
                            "La fila " + (row + 1) + " no tiene un ID v치lido (columna vac칤a).",
                            "Advertencia",
                            JOptionPane.WARNING_MESSAGE
                    );
                    continue;
                }

                String idLote = valorCelda.toString();

                boolean ok = dao.eliminar(idLote);
                if (ok) {
                    model.removeRow(row);
                    eliminados++;
                } else {
                    JOptionPane.showMessageDialog(
                            this,
                            "No se pudo eliminar el lote con ID: " + idLote,
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                    );
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(
                        this,
                        "Error inesperado al eliminar la fila " + (row + 1) + ": " + e.getMessage(),
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                );
            }
        }

        if (eliminados > 0) {
            JOptionPane.showMessageDialog(this, eliminados + " Lote(s) eliminadas correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "No se elimin칩 ninguna lote.");
        }

    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona un lote de la tabla para editarlo.");
            return;
        }

        // Supongamos que la columna 0 tiene el n칰mero de identificaci칩n
        String idLote = jTable1.getValueAt(fila, 0).toString();

        // Buscar el Lote en la BD
        try {
            // Buscar el Lote en la BD
            Clases.dao.LoteDAO buscarId = new Clases.dao.LoteDAO();
            Clases.modelo.Lote lote = buscarId.buscarPorId(idLote);

            if (lote != null) {
                // Crear el panel EditLote y pasarle el lote
                EditLote panelEditar = new EditLote();
                panelEditar.setLote(lote);

                // Mostrar el panel en el Dashboard
                Dashboard.ShowJPanel(panelEditar);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr칩 la lote seleccionado.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al obtener el lote: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

