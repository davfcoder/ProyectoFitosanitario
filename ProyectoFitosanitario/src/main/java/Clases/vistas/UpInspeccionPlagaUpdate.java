    private void LoadPlagasAsociadas() {
        try {
            InspeccionPlagaDAO dao = new InspeccionPlagaDAO();
            DefaultTableModel model = (DefaultTableModel) tblPlagasAsociadas.getModel();
            model.setRowCount(0); // limpia la tabla

            // Llamamos al nuevo m茅todo DAO que devuelve la lista de objetos
            dao.listarPlagasInspeccion(IdInspeccion).forEach((plaga) -> {
                model.addRow(new Object[]{
                    plaga.getIdPlaga(),
                    plaga.getNomEspecie(),             // Muestra el nombre
                    plaga.getCantidadPlantasInfestadas(),
                    plaga.getPorcentajeInfestacion()
                });
            });
            
            //  Ocultar la primera columna (id_usuario)
            tblPlagasAsociadas.getColumnModel().getColumn(0).setMinWidth(0);
            tblPlagasAsociadas.getColumnModel().getColumn(0).setMaxWidth(0);
            tblPlagasAsociadas.getColumnModel().getColumn(0).setWidth(0);
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(this, "Error al cargar la tabla de plagas: " + e.getMessage());
        }
    }
