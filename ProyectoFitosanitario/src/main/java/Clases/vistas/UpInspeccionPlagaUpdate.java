    private void btnEditarPlagaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarPlagaActionPerformed
        int plagaS = tblPlagasAsociadas.getSelectedRow();
        if (plagaS == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una plaga de la tabla para editar.");
            return;
        }

        // Supongamos que la columna 0 tiene el id
        String idPlagaS = tblPlagasAsociadas.getValueAt(plagaS, 0).toString();
        String nomPlagaS = tblPlagasAsociadas.getValueAt(plagaS, 1).toString();
        int cantidadPlagaS = (int) tblPlagasAsociadas.getValueAt(plagaS, 2);
        // Buscar la plaga en la BD
        try {
            UpInspeccionPlagaExtra panelEditar = new UpInspeccionPlagaExtra(true, inspeccionActual, idPlagaS, nomPlagaS, cantidadPlagaS);
            // Mostrar el panel en el Dashboard
            Dashboard.ShowJPanel(panelEditar);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al obtener la plaga: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEditarPlagaActionPerformed


