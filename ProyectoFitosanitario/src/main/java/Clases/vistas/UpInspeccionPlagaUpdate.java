    private void btnEliminarPlagaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarPlagaActionPerformed
        InspeccionPlagaDAO daoIP = new InspeccionPlagaDAO();
        DefaultTableModel model = (DefaultTableModel) tblPlagasAsociadas.getModel();
        
        int [] selected = tblPlagasAsociadas.getSelectedRows();
        
        if (selected.length < 1) {
            JOptionPane.showMessageDialog(this,
                "Debes seleccionar una o m谩s plagas para eliminar.",
                "Sin plaga seleccionada",
                JOptionPane.WARNING_MESSAGE);
        } else {
            int confirmacion = JOptionPane.showConfirmDialog(
                    this,
                    "驴Est谩s seguro de eliminar las plagas seleccionadas?",
                    "Confirmar eliminaci贸n",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE
            );
            if (confirmacion == JOptionPane.YES_OPTION){
                int eliminados = 0;
                java.util.Arrays.sort(selected);
                for (int idx = selected.length - 1; idx >= 0; idx--){
                    int row = selected[idx];
                    try {
                        String idPlagaSelec = tblPlagasAsociadas.getValueAt(row, 0).toString();
                        String nomPlagaSelec = tblPlagasAsociadas.getValueAt(row, 1).toString();
                        boolean ok = daoIP.eliminar(IdInspeccion, idPlagaSelec);
                        
                        if (ok) {
                            model.removeRow(row);
                            eliminados++;
                        } else {
                            JOptionPane.showMessageDialog(
                                    this,
                                    "No se pudo eliminar la plaga con nombre: " + nomPlagaSelec,
                                    "Error",
                                    JOptionPane.ERROR_MESSAGE
                            );
                        }
                    } catch (Exception e) {
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(
                                this,
                                "Error inesperado al eliminar la plaga en la fila " + row + ": " + e.getMessage(),
                                "Error",
                                JOptionPane.ERROR_MESSAGE
                        );
                    }
                }
                if (eliminados >0){
                    JOptionPane.showMessageDialog(
                            this,
                            eliminados + " plaga(s) eliminada(s) correctamente."
                    );
                } else {
                    JOptionPane.showMessageDialog(
                            this,
                            "No se elimin贸 ninguna plaga asociada a la inspecci贸n."
                    );
                }
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "Eliminaci贸n cancelada por el usuario."
                );
            }
        }
    }//GEN-LAST:event_btnEliminarPlagaActionPerformed

