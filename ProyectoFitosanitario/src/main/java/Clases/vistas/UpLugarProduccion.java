    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombre.getText().trim().isEmpty()
                || txtRegistroICA.getText().trim().isEmpty()
                || jBoxUsuarioProductor.getSelectedIndex() <= 0) {

            JOptionPane.showMessageDialog(this,
                    "Por favor, complete todos los campos antes de guardar.",
                    "Campos incompletos",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // üîπ Obtener el nombre seleccionado
            String nombreUsuario = (String) jBoxUsuarioProductor.getSelectedItem();

            // üîπ Buscar ID del departamento
            Clases.dao.UsuarioDAO usuDAO = new Clases.dao.UsuarioDAO();
            String idUsu = usuDAO.obtenerIdUsuarioPorNombre(nombreUsuario);

            if (idUsu == null) {
                JOptionPane.showMessageDialog(this,
                        "No se encontr√≥ el ID del usuario seleccionado.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            // üîπ Crear lugar produccion con el ID correcto
            LugarProduccion lugarproduccion = new LugarProduccion();
            lugarproduccion.setNomLugarProduccion(txtNombre.getText());
            lugarproduccion.setNroRegistroICA(txtRegistroICA.getText());
            lugarproduccion.setIdUsuarioProductor(idUsu); // ‚úÖ Guardamos el ID real

            // üîπ Insertar en la BD
            Clases.dao.LugarProduccionDAO lugarproduccionDAO = new Clases.dao.LugarProduccionDAO();
            boolean guardado = lugarproduccionDAO.insertar(lugarproduccion);

            if (guardado) {
                JOptionPane.showMessageDialog(this, "Lugar de produccion guardado correctamente");
                limpiarCampos();
                Dashboard.ShowJPanel(new GestionLugaresProduccion());
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo guardar el Lugar de produccion");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
            e.printStackTrace();
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

