    private void LoadEspecieVLP() {
        try {
            LugarEspecieDAO dao = new LugarEspecieDAO();
            DefaultTableModel model = (DefaultTableModel) tblEspeciesLP.getModel();
            model.setRowCount(0); // limpia la tabla

            // Llamamos al nuevo m茅todo DAO que devuelve la lista de objetos
            dao.listarEspeciesAsociadasConDetalles(idLugarProduccion).forEach((especie) -> {
                model.addRow(new Object[]{
                    especie.getIdEspecie(),
                    especie.getNomComun(),
                    especie.getAreaDestCultivo(),
                    especie.getCapacidadProduccionMax()
                });
            });
            
            //  Ocultar la primera columna (id_usuario)
            tblEspeciesLP.getColumnModel().getColumn(0).setMinWidth(0);
            tblEspeciesLP.getColumnModel().getColumn(0).setMaxWidth(0);
            tblEspeciesLP.getColumnModel().getColumn(0).setWidth(0);
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(this, "Error al cargar la tabla de especies vegetales disponibles: " + e.getMessage());
        }
    }
    

