    private void btnEditarProyeccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarProyeccionActionPerformed
        int especieS = tblEspeciesLP.getSelectedRow();
        if (especieS == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una plaga de la tabla para editar.");
            return;
        }
        int [] especiesS = tblEspeciesLP.getSelectedRows();
        if (especiesS.length>1){
            JOptionPane.showMessageDialog(this, "Por favor, seleccione SOLO UNA especie de la tabla para continuar.");
            return;
        }
        // Supongamos que la columna 0 tiene el id
        String idEspecieS = tblEspeciesLP.getValueAt(especieS, 0).toString();
        EspecieVegetalDAO daoEspecie = new EspecieVegetalDAO();
        Clases.modelo.EspecieVegetal especieV = daoEspecie.buscarPorId(idEspecieS);
        float areaCultivoS = (float) tblEspeciesLP.getValueAt(especieS, 2);
        float capacidadMaxS = (float) tblEspeciesLP.getValueAt(especieS, 3);
        // Buscar la plaga en la BD
        try {
            UpLugarProdRegProyecContinuar panelEditar = new UpLugarProdRegProyecContinuar(true, lugarproduccionActual, especieV, areaCultivoS, capacidadMaxS);
            // Mostrar el panel en el Dashboard
            Dashboard.ShowJPanel(panelEditar);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error al obtener la plaga: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEditarProyeccionActionPerformed

    private void btnEliminarProyeccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProyeccionActionPerformed
        LugarEspecieDAO daoLP = new LugarEspecieDAO();
        DefaultTableModel model = (DefaultTableModel) tblEspeciesLP.getModel();

        int [] selected = tblEspeciesLP.getSelectedRows();

        if (selected.length < 1) {
            JOptionPane.showMessageDialog(this,
                "Debes seleccionar una o m谩s especies (proyecciones) para eliminar.",
                "Sin especie seleccionada",
                JOptionPane.WARNING_MESSAGE);
        } else {
            int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "驴Est谩s seguro de eliminar la(s) proyecci贸n(es) seleccionada(s)?",
                "Confirmar eliminaci贸n",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
            );
            if (confirmacion == JOptionPane.YES_OPTION){
                int eliminados = 0;
                java.util.Arrays.sort(selected);
                for (int idx = selected.length - 1; idx >= 0; idx--){
                    int row = selected[idx];
                    try {
                        String idEspecieS = tblEspeciesLP.getValueAt(row, 0).toString();
                        String nomEspecieSelec = tblEspeciesLP.getValueAt(row, 1).toString();
                        boolean ok = daoLP.eliminar(idLugarProduccion, idEspecieS);

                        if (ok) {
                            model.removeRow(row);
                            eliminados++;
                        } else {
                            JOptionPane.showMessageDialog(
                                this,
                                "No se pudo eliminar la especie (proyecci贸n) con nombre: " + nomEspecieSelec,
                                "Error",
                                JOptionPane.ERROR_MESSAGE
                            );
                        }
                    } catch (Exception e) {
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(
                            this,
                            "Error inesperado al eliminar la especie en la fila " + row + ": " + e.getMessage(),
                            "Error",
                            JOptionPane.ERROR_MESSAGE
                        );
                    }
                }
                if (eliminados >0){
                    JOptionPane.showMessageDialog(
                        this,
                        eliminados + " especie(s) eliminada(s) correctamente."
                    );
                } else {
                    JOptionPane.showMessageDialog(
                        this,
                        "No se elimin贸 ninguna especie asociada a la inspecci贸n."
                    );
                }
            } else {
                JOptionPane.showMessageDialog(
                    this,
                    "Eliminaci贸n cancelada por el usuario."
                );
            }
        }
    }//GEN-LAST:event_btnEliminarProyeccionActionPerformed


