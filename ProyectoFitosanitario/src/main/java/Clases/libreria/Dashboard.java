    // Control de visibilidad seg煤n Rol
    private void configurarMenuSegunRol() {
        lblBienvenida.setText("Bienvenido: " + nombreUsuario);
        lblRol.setText("Rol: " + nombreRol);

        switch (nombreRol) {
            case "Administrador ICA":
                break;
            case "Propietario":
                btnFitosanitario.setVisible(false);
                btnGestionarUsuarios.setVisible(false);
                btnProduccion.setVisible(false);
                break;
            case "Productor":
                btnFitosanitario.setVisible(false);
                btnProduccion.setVisible(false);
                btnGestionarUsuarios.setVisible(false);
                btnReportes.setVisible(false);
                break;
            case "Asistente T茅cnico":
                btnFitosanitario.setVisible(false);
                btnReportes.setVisible(false);
                btnGestionarUsuarios.setVisible(false);
                break;
        }
    }
